import { readFileSync, writeFileSync, existsSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const envPath = join(root, '.env');
const outPath = join(root, 'firebase-config.js');

const PLACEHOLDER = 'YOUR_API_KEY';

const ENV_KEYS = [
    'FIREBASE_API_KEY',
    'FIREBASE_AUTH_DOMAIN',
    'FIREBASE_PROJECT_ID',
    'FIREBASE_STORAGE_BUCKET',
    'FIREBASE_MESSAGING_SENDER_ID',
    'FIREBASE_APP_ID',
    'FIREBASE_MEASUREMENT_ID'
];

function parseEnvFile(contents) {
    const env = {};
    for (const line of contents.split('\n')) {
        const trimmed = line.trim();
        if (!trimmed || trimmed.startsWith('#')) continue;
        const eq = trimmed.indexOf('=');
        if (eq === -1) continue;
        const key = trimmed.slice(0, eq).trim();
        let value = trimmed.slice(eq + 1).trim();
        if ((value.startsWith('"') && value.endsWith('"')) ||
            (value.startsWith("'") && value.endsWith("'"))) {
            value = value.slice(1, -1);
        }
        env[key] = value;
    }
    return env;
}

function loadEnv() {
    const fromFile = existsSync(envPath) ? parseEnvFile(readFileSync(envPath, 'utf8')) : {};
    const merged = { ...fromFile };

    // Vercel (and other CI) inject vars into process.env — they override .env
    for (const key of ENV_KEYS) {
        if (process.env[key]) {
            merged[key] = process.env[key];
        }
    }

    return merged;
}

const env = loadEnv();
const source = process.env.VERCEL ? 'Vercel environment variables' : (existsSync(envPath) ? '.env' : 'defaults');

const firebaseConfig = {
    apiKey: env.FIREBASE_API_KEY || PLACEHOLDER,
    authDomain: env.FIREBASE_AUTH_DOMAIN || 'YOUR_PROJECT.firebaseapp.com',
    projectId: env.FIREBASE_PROJECT_ID || 'YOUR_PROJECT_ID',
    storageBucket: env.FIREBASE_STORAGE_BUCKET || 'YOUR_PROJECT.appspot.com',
    messagingSenderId: env.FIREBASE_MESSAGING_SENDER_ID || 'YOUR_SENDER_ID',
    appId: env.FIREBASE_APP_ID || 'YOUR_APP_ID',
    measurementId: env.FIREBASE_MEASUREMENT_ID || ''
};

const output = `// Auto-generated by scripts/generate-firebase-config.mjs — do not edit or commit.
// Local: set values in .env | Vercel: Project Settings → Environment Variables

export const firebaseConfig = ${JSON.stringify(firebaseConfig, null, 4)};

export function isFirebaseConfigured() {
    return Boolean(
        firebaseConfig.apiKey &&
        firebaseConfig.apiKey !== '${PLACEHOLDER}' &&
        firebaseConfig.projectId &&
        firebaseConfig.projectId !== 'YOUR_PROJECT_ID'
    );
}
`;

writeFileSync(outPath, output, 'utf8');

const configured = Boolean(
    firebaseConfig.apiKey &&
    firebaseConfig.apiKey !== PLACEHOLDER &&
    firebaseConfig.projectId &&
    firebaseConfig.projectId !== 'YOUR_PROJECT_ID'
);

if (configured) {
    console.log(`Generated firebase-config.js from ${source}`);
} else if (process.env.VERCEL) {
    console.error('Build on Vercel: add FIREBASE_* environment variables in Project Settings → Environment Variables');
    process.exit(1);
} else {
    console.warn('Generated firebase-config.js (placeholders — copy .env.example to .env and add your Firebase values)');
}
